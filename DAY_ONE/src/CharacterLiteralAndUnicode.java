public class CharacterLiteralAndUnicode {
    public static void demonstrate() {
        char ch='a';
        System.out.println(ch);

        char ch1=65;

        System.out.println(ch1);

        int name='A';

        System.out.println(name);

        char var1='\u20AC';

        System.out.println(var1);
    }
}
